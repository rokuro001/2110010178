    private void btntambahdataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahdataActionPerformed

        DataPemilik.insertNik(Integer.parseInt(txtNik.getText()));
        DataPemilik.inserttanggal(txtTgl.getText());
        DataPemilik.insertNama(txtNama.getText());
        DataPemilik.insertkelurahan(txtKel.getText());
        DataPemilik.insertalamat(txtAlamat.getText());
        DataPemilik.insertRT(txtRt.getText());
        DataPemilik.insertRW(txtRw.getText());
        DataPemilik.insertjenis_kelamin(cmbJK.getSelectedItem().toString());
        DataPemilik.insertagama(cmbAgama.getSelectedItem().toString());
        DataPemilik.insertpekerjaan(txtPekerjaan.getText());
        DataPemilik.insertKwarganegaraan(txtKW.getText());
        JOptionPane.showMessageDialog(this, "Data Berhasil Di Tambah");

        // TODO add your handling code here:
    }//GEN-LAST:event_btntambahdataActionPerformed

