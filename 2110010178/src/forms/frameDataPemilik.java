        String isiArray ="NIK : "+DataPemilik.getRecordNik().get(i)+
                       "\nTanggal : "+DataPemilik.getRecordtanggal().get(i)+
                       "\nNama : "+DataPemilik.getRecordNama().get(i)+
                       "\nKelurahan : "+DataPemilik.getRecordkelurahan().get(i)+
                       "\nAlamat : "+DataPemilik.getRecordalamat().get(i)+
                       "\nRt : "+DataPemilik.getRecordRT().get(i)+
                       "\nRw : "+DataPemilik.getRecordRW().get(i)+
                       "\nJenis Kelamin : "+DataPemilik.getRecordjenis_kelamin().get(i)+
                       "\nAgama : "+DataPemilik.getRecordagama().get(i)+
                       "\nPekerjaan : "+DataPemilik.getRecordpekerjaan().get(i)+
                       "\nKwarganegaraan : "+DataPemilik.getRecordKwarganegaraan().get(i);

        JOptionPane.showMessageDialog(this, isiArray);

